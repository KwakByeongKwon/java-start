package variable;

public class Var5 {
    // 한번에 여러개 선언도 가능 ,로 구분 int a, int b;
    // 선언과 동시에 값을 초기화 시켜줄수도있음
    // int d = 10, e = 10;
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(1);

        int b = 2;
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(3);
        System.out.println(4);
    }

}
