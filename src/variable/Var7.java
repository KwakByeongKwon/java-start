package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 true/false 입력 가능
        char d = 'A'; // 문자하나 '' 로 묶어줌
        String e = "Hello Java"; // 문자열

        // 각 타입에 맞는것을 사용해야함
        /*
        코드에서 개발자가 직접 적은 100,10.5,true,'A',"Hello Java"와 같은 고정된 값을
        프로그래밍 용어로 리터럴(literal)이라고 합니다.
         */
    }
}
